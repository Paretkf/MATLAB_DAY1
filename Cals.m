function avgTemp = Cals(fname, day, month)
% cal avg 1st May temp
Data = importfile(fname);
readData();

%% 1. ???? Timestamp ???????????
Data.Date = datevec ( Data.Timestamp);
%% 2. ???? F ???? C
Data.OATC = 5/9*(Data.OATF-32);
%% 3. ??????????? 1 ???????
cond = (Data.Date (:, 3) == day) & (Data.Date (:, 2) == month);
data = Data(cond, :);
%% 4. ??????????????????????
avgTemp = mean( data.OATC);
disp(avgTemp);
plot(data.Timestamp, data.OATC);